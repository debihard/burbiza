  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
  echo -e "\e[93m[+]\e[00m Extract a1 archive. Please Enter Your Password!"
  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"

  yum -y install p7zip
  echo ""
  echo -n " Please Enter Your Password: "; read -s archivepassword
