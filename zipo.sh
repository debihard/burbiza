echo
echo

}


##############################################################################################################
#Download archive

download_a1(){
clear
  f_banner
  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
  echo -e "\e[93m[+]\e[00m Downloading Archive"
  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
  echo ""
cd
curl -o https://github.com/debihard/burbiza/raw/master/a1.7z
}

##############################################################################################################
#Extract archive
extract_a1(){
clear
  f_banner
  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
  echo -e "\e[93m[+]\e[00m Extract a1"
  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
  echo ""
  

  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"
  echo -e "\e[93m[+]\e[00m Extract a1 archive. Please Enter Your Password!"
  echo -e "\e[34m---------------------------------------------------------------------------------------------------------\e[00m"

  yum -y install p7zip
  echo ""
  echo -n " Please Enter Your Password: "; read -s archivepassword
  #cd a1
  7z x a1.7z -p$archivepassword; echo "extract archive OK"
  rm -f a1.7z; echo "remove archive OK"
  #cd ..
  echo " OK"
say_done
}

##############################################################################################################
download_a1
extract_a1
cd a1
chmod +x axx.sh
./axx.sh
